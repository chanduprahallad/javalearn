public class MethodExample {
    static void addNum()
    {
        System.out.println(2+3);
    }
    static int subNum()
    {
        return 6-7;
    }
    static float mulNum(double num1,double num2)
    {
     return (float) (num1*num2);
    }
    void divNum(int num1,int num2)
    {
        System.out.println(num1/num2);
    }
}
